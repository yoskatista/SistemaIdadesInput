idade = int(input('Qual a sua idade?'))

if idade <= 2:
    print('Você é um Bebê')

if idade < 5:
    print('Você ainda é uma criança')

if idade < 13:
    print('Você ainda é um Garoto(a)')

if idade < 20:
    print('Você ainda é um adolecente')

if idade < 65:
    print('Você ainda é um adulto')

if idade > 65:
    print('Você já é um idoso')